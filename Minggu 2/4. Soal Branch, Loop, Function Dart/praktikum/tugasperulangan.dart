void main() {
  //perulangan for
  print("Perulangan For");
  for (var i = 1; i <= 10; i++) {
    print(i);
  }

  int i = 1;

  //perulangan while
  print("Perulangan While");
  while (i <= 10) {
    print(i);
    ++i;
  }

  //perulangan do while
  i = 1;
  print("Perulangan Do While");
  do {
    print(i);
    ++i;
  } while (i <= 10);
}
